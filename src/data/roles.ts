/** 角色分类：有完整 persona 的为"剧情角色"，其余为"基础角色" */
export type RoleCategory = 'basic' | 'story';

export function getRoleCategory(role: Role): RoleCategory {
  return role.persona?.trim() ? 'story' : 'basic';
}

/** 按分类分组，保持原始顺序 */
export function getRolesByCategory(): { basic: Role[]; story: Role[] } {
  const basic: Role[] = [];
  const story: Role[] = [];
  for (const r of ROLES) {
    (getRoleCategory(r) === 'story' ? story : basic).push(r);
  }
  return { basic, story };
}

/**
 * 把 persona 文本按"标签：内容"的分段方式拆成结构化片段。
 * 形如 "身份：32岁..." → { label: '身份', content: '32岁...' }
 * 没有标签的开头段落（如"核心设定：..."前的引导句）归入前一段或独立成段。
 */
export interface PersonaSection {
  label: string;
  content: string;
}

export function parsePersona(persona: string): PersonaSection[] {
  const lines = persona.split('\n');
  const sections: PersonaSection[] = [];
  let current: PersonaSection | null = null;

  for (const raw of lines) {
    const line = raw;
    // 形如 "身份：..." 或 "外貌与习惯：..."
    const m = line.match(/^([^\s：]{2,8})：(.*)$/);
    if (m) {
      if (current) sections.push({ ...current, content: current.content.trim() });
      current = { label: m[1], content: m[2].trim() };
    } else if (current) {
      current.content += (current.content ? '\n' : '') + line;
    } else if (line.trim()) {
      current = { label: '', content: line };
    }
  }
  if (current) sections.push({ ...current, content: current.content.trim() });
  return sections.filter((s) => s.content);
}